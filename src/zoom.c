#include "fractol.h"

void	zoom_in(t_fractol *f)
{
	f->start_x += 0.35;
	f->start_y -= 0.35;
	f->end_x -= 0.35;
	f->end_y += 0.35;
}

void	zoom_out(t_fractol *f)
{
	f->start_x -= 0.35;
	f->start_y += 0.35;
	f->end_x += 0.35;
	f->end_y -= 0.35;
}